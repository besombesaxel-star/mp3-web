const LUCIDE_ICONS: Partial<Record<Platform, typeof Youtube>> = {
  YouTube: Youtube,
  GitHub: Github,
  Twitch: Twitch,
  Instagram: Instagram,
};

export function PlatformIcon({
  url,
  size = 15,
  className,
}: {
  url: string;
  size?: number;
  className?: string;
}) {
  const platform = detectPlatform(url);

  if (!platform) {
    return <Link2 size={size} className={className} />;
  }

  const LucideIcon = LUCIDE_ICONS[platform];
  if (LucideIcon) {
    return <LucideIcon size={size} className={className} />;
  }

  const path = BRAND_PATHS[platform];
  if (!path) {
    return <Link2 size={size} className={className} />;
  }

  return (
    <svg
      role="img"
      viewBox="0 0 24 24"
      width={size}
      height={size}
      className={className}
      fill="currentColor"
      aria-label={platform}
    >
      <path d={path} />
    </svg>
  );
}
